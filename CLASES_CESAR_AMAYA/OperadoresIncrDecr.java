
public class OperadoresIncrDecr {
    public static void main(String[] args) {
        int a =  10;
        int b =  10;
        System.out.println("a++: " + a++);//1°Evalua con a y luego incrementa
        System.out.println("++b: " + ++b);//1° Incrementa y luego incrementa
        System.out.println("a: " + a);//Comprueba que el valor se haya incrementado
        System.out.println("b: " + b);//Comprueba que el valor se haya incrementado
    }


}
